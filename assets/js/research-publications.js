function optimize3dScholarLink(title) {
  return `https://scholar.google.com/scholar?q=${encodeURIComponent(title).replace(/%20/g, "+")}`;
}

function optimize3dPublicationText(paper, field, locale) {
  if (locale !== "en") {
    return paper[field];
  }

  if (field === "journal") {
    return optimize3dJournalEn[paper.journal] || paper.journal;
  }

  if (field === "title") {
    return optimize3dTitleEn[paper.title] || paper.title;
  }

  return paper[field];
}

function appendPublicationCell(row, text) {
  const cell = document.createElement("span");
  cell.setAttribute("role", "cell");
  cell.textContent = text;
  row.appendChild(cell);
  return cell;
}

function appendPublicationRow(table, paper, locale) {
  const row = document.createElement("div");
  row.setAttribute("role", "row");

  appendPublicationCell(row, paper.year);
  appendPublicationCell(row, optimize3dPublicationText(paper, "journal", locale));

  const titleCell = document.createElement("span");
  titleCell.setAttribute("role", "cell");
  const displayTitle = optimize3dPublicationText(paper, "title", locale);
  const link = document.createElement("a");
  link.className = "publication-title-link";
  link.href = optimize3dScholarLink(displayTitle);
  link.target = "_blank";
  link.rel = "noreferrer";
  link.textContent = displayTitle;
  titleCell.appendChild(link);
  row.appendChild(titleCell);

  table.appendChild(row);
}

function renderOptimize3DPapers() {
  document.querySelectorAll("[data-publication-table]").forEach((table) => {
    const locale = table.dataset.locale || "ko";
    const labels = locale === "en"
      ? ["Year", "Journal", "Paper Title"]
      : ["연도", "학술지", "논문 제목"];

    table.textContent = "";

    const header = document.createElement("div");
    header.setAttribute("role", "row");
    labels.forEach((label) => {
      const cell = document.createElement("span");
      cell.setAttribute("role", "columnheader");
      cell.textContent = label;
      header.appendChild(cell);
    });
    table.appendChild(header);

    optimize3dPapers.forEach((paper) => appendPublicationRow(table, paper, locale));
  });
}

renderOptimize3DPapers();
